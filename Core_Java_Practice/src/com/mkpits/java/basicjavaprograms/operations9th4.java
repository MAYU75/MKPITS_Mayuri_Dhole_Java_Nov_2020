//Java program to print the result of the specified operations.
//-1 + 4 * 6
//( 35+ 5 ) % 7
//14 + -4 * 6 / 11
//2 + 15 / 6 * 1 - 7 % 2
class operations9th4
{
	public static void main(String[] args)
	{
		int result1;
		float result2,result3,result4;
		result1= -1 + 4 * 6;
		result2= ( 35+ 5 ) % 7;
		result3= 14 + -4 * 6 / 11;
		result4= 2 + 15 / 6 * 1 - 7 % 2;
		System.out.println("-1 + 4 * 6 = "+ result1);
		System.out.println("( 35+ 5 ) % 7 = "+ result2);
		System.out.println("14 + -4 * 6 / 11 = "+ result3);
		System.out.println("2 + 15 / 6 * 1 - 7 % 2 = "+ result4);
	}
}
