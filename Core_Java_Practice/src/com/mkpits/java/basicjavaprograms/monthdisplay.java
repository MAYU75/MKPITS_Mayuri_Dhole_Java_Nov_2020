//Java program using switch to display month name

package src.com.mkpits.java.basicjavaprograms;

class monthdisplay {
    public static void main(String[] args) {
        int mon = 5;
        String month = null;
        switch (mon) {
            case 1:
                month = "January";
                break;
            case 2:
                month = "February";
                break;
            case 3:
                month = "March";
                break;
            case 4:
                month = "April";
                break;
            case 5:
                month = "May";
                break;
            case 6:
                month = "June";
                break;
            case 7:
                month = "July";
                break;
            case 8:
                month = "August";
                break;
            case 9:
                month = "September";
                break;
            case 10:
                month = "October";
                break;
            case 11:
                month = "November";
                break;
            case 12:
                month = "December";
                break;
            default:
                System.out.println("Wrong choice");
        }
        System.out.println("Given month is " + month);
    }
}